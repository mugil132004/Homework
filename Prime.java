import java.util.Scanner;
class Prime
{
	public static void main(String[] args)
	{
		Scanner sc = new Scanner(System.in);
		{
			System.out.print("enter the number : ");
			int num = sc.nextInt();
		
		if(num/num==1)
		{
			System.out.println("number is prime");
			}
		
				
		else
		{
			System.out.println("number is not prime");
		}		
	}
}} 